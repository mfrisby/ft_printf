#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	i;
	int	e;

	e = 0;
	if (s != NULL && f != NULL)
	{
		i = ft_strlen(s);
		while (i > e)
		{
			(*f)(s);
			i--;
			s++;
		}
	}
}
